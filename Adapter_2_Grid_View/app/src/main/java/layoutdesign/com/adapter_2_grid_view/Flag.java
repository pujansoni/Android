class Flag {
    private int image;
    private String flag;
    public Flag(Integer image, String flag) {
        this.image = image;
        this.flag = flag;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }
}

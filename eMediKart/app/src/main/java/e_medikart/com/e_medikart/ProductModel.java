public class ProductModel {
    String productId;
    String productName;
    String productUse;
    String productPhoto;
    String productSideEffects;
    String productContent;
    String productPregsafety;
    String productPrice;
    String manufacturerName;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductUse() {
        return productUse;
    }

    public void setProductUse(String productUse) {
        this.productUse = productUse;
    }

    public String getProductPhoto() {
        return productPhoto;
    }

    public void setProductPhoto(String productPhoto) {
        this.productPhoto = productPhoto;
    }

    public String getProductSideEffects() {
        return productSideEffects;
    }

    public void setProductSideEffects(String productSideEffects) {
        this.productSideEffects = productSideEffects;
    }

    public String getProductContent() {
        return productContent;
    }

    public void setProductContent(String productContent) {
        this.productContent = productContent;
    }

    public String getProductPregsafety() {
        return productPregsafety;
    }

    public void setProductPregsafety(String productPregsafety) {
        this.productPregsafety = productPregsafety;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getManufacturerName() {
        return manufacturerName;
    }

    public void setManufacturerName(String manufacturerName) {
        this.manufacturerName = manufacturerName;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }
}
